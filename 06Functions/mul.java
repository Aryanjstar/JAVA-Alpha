

public class mul {
    public static int mul(int a, int b){
        
        int mul = a*b;
        
        return mul;
    }    
    public static void main(String [] args){
        int a = 10;
        int b = 5;
        mul(a,b);
        System.out.println("Multiplication Is: " + mul(a, b));
        System.out.println("Multiplication Is: " + mul(5, 2));

    }
}
